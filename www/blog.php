                    <h3>Metal Fabrication</h3>
                </div>                
                <div class="blog-body">                    
                    <p>
                    In the manufacturing industry, different metal tools and parts are used for different purposes, from medical supplies to kitchen silverware and widely in the aerospace and building industries. When making a decision on the material to use for a metal fabrication, the following must be carefully considered:
                    </p>

                    <ul>
                        <li>The cost of the end product</li>
                        <li>The location and application of the product</li>
                        <li>Corrosion, rusting, and staining</li>
                        <li>Flexibility, weight and aesthetics</li>
                    </ul>

                    <p>	
                    The two most common materials in consideration for metal fabrications are aluminum and steel. Each has its pros and cons all of which should be carefully weighed as you make your decision.
                    </p>
                    <p>
                    <b>Aluminum </b>
                    </p>
                    <p>
                    Aluminum is easily malleable therefore, it makes the best material for spinning and constructing products that have intricate shapes; it is less brittle than steel and is less likely to crack or rip apart during the spinning process. Because it is a strong lightweight material, aluminum is widely used in the aerospace industry. The biggest draw of aluminum is its resistance to rusting without coating and painting, even if it is to be exposed to abrasive conditions. Even though it is about 1/3 the density of steel, aluminum offers versatility in the fabrication process.  What hampers its use the most is the cost of production. The price of the constituent materials, iron and bauxite ore will affect the final cost of the product, but because aluminum has several recyclable characteristics, it becomes a more viable choice.  
                    </p>
                    <p>
                    <b>Steel</b>
                    </p>
                    <p>
                    The steel industry uses about 1.3 billion tons of steel every year, and it is the most common material used in architecture. There are different sub-groups of steel and a type is chosen depending on the nature of the work to be done. Steel is generally stronger and denser than aluminum and can withstand incredible amount of heat‘Mild steel’ as carbon steel is also known, is steel containing not more than 2% carbon and no other alloy. Mild steel is magnetic so it is very commonly used in making electrical materials. It is very dense and welding requires precautions to prevent cracking. Carbon steel is corrosive due to the presence of iron oxide and should be coated if the product is to be exposed to moisture and air. If the metal is to be bent or welded or if the exterior of the final product is not going to be seen, use carbon steel.
                    </p>
                    <p>
                    Stainless steel is an alloy of chromium, nickel, molybdenum and very little carbon along with some other elements to improve the mechanical and chemical properties of steel.  The main advantage of stainless steel is that it is not susceptible to corrosion because chromium oxide which is nonreactive forms in place of iron oxide. It is more aesthetically pleasing than mild steel. It is harder to weld and it is more brittle than carbon steel, it is also more expensive. Kitchen, cleanroom and medical equipment is best made with stainless steel.
                    Based on your budget and project type, you can now go on to choose the material that would be most effective for you.  
                    </p>
