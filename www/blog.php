                    <h3>CNC (Computer Numerical Control) Cutting </h3>
                </div>                
                <div class="about-text">
                    <p>The choice of what type of cutting machine to buy depends on what is most important to you, and what is most valuable to you. Whichever you pick in the end will be decided by:</p>

                    <ul>
                        <li>Precision</li>
                        <li>Speed</li>
                        <li>Relative cost, and</li>
                        <li>Flexibility (variety of materials to be cut) that you require</li>
                    </ul>

                    <p>
                    <b>Oxy-fuel Cutting</b>
                    </p>
                    <p>
                    Flame cutting is by far the oldest of all the methods. It is most efficient when used to cut mild steel. The machine is relatively cheap and accessible; it is also deemed the ‘simplest´ both in size of equipment and use. Oxy-flame cuts thick sheets of steel from about 1 inch and will not cut sizes less than ¼ inches. The cutting process is quite slow but can be sped up with the use of multiple flames which need not be interrupted as it can go on for as long as oxygen is available. Its drawbacks are that it is not flexible and cutting precise shapes is almost impossible because of the heat that causes some parts of the sheet to be rounded causing wastage of the material.
                    </p>

                    <p>
                    <b>Plasma Cutting</b>
                    </p>
                    <p>
                    This is used to cut metallics like stainless steel, mild steel and aluminum. Using this cutter ensures that production takes place at a faster rate than with the oxy-fuel, it can also cut thinner plates (1/4 to 1/5 inches), but when the plates are less than ¼ inches, the edges of the plate begins to suffer some warp due to the heat, this can be avoided if the process is carried out under water though. The equipment for this method of cutting is more expensive and cumbersome than the flame costing around $15,000 to $100,000 and because of the cost and size, the number of plasma cutters that can be used at once is limited to maybe only two.
                    </p>

                    <p>
                    <b>Laser Cutting</b>
                    </p>
                    <p>
                    This cutting process is slower because instead of using preheat, it uses a highly focused flame beam. The speed of the process is limited to the rate of the reaction between oxygen and carbon. The machine is quite expensive, with new ones going for about $350,000 and fairly used for about $250,000. 
                    </p>
                    <p>
                    The kerf width and the quality of the edges cut are very precise with limited serration or wastage. The machine also has accurate automation that can allow you to set everything up, start the process and go home for the night, returning to meet neat piles of cut material for you. Laser can be used to cut fiberglass, some plastic and even fabric.   
                    </p>
                    <p>
                    <b>Waterjet Cutting</b>
                    </p>
                    <p>
                    This is the most efficient method used in cutting. The waterjet can cut literally anything that can be fit between it jets. It cannot cut diamond however, as it is too hard, it also cannot cut tempered glass or laminated ceramics because tempered glass would shatter and water will get between the laminated layers of the ceramics. The waterjet however has the best precision and can cut intricate shapes with the kerf width as narrow as 0.035 and there is no wastage or warp. The equipment is expensive and can cost about $100,000 to $350,000. It has a wide thickness range but cutting is slower and because of tendency of water to diverge, the quality produced may not be as refine.
                    </p>
                    <p>
                    Careful deliberation and consultation will ensure that you get the cutter that will be best for you.
                    </p>
